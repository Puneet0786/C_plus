#include<iostream>
using namespace std;

int main() {

    int a; //declaration
    a=12; //initialisation

    cout<<"size of int "<<sizeof(a)<<endl;

    float b;
    cout<<"size of float "<<sizeof(b)<<endl;

    char c;
    cout<<"size of char "<<sizeof(c)<<endl;

    bool d;
    cout<<"size of bool "<<sizeof(d)<<endl;

    short int s;
    cout<<"size of shortint "<<sizeof(s)<<endl;

    long int l;
    cout<<"size of longint "<<sizeof(l)<<endl;
}